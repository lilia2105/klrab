public class ot1do10000 {//Создаем публичный класс с именем z3_5
    public static void main (String[] args) {//Этот метод `main` является точкой входа в программу.
        int i =0;//выполняется присваивание значения переменной i
        while (i != 10000) {//пока переменная i не будет равна 10000, программа будет увеличивать переменную на 1 единицу
            i++;
            System.out.println(i);//здесь происходит вывод переменной i
        }
    }
}

